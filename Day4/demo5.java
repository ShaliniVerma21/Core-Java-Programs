package Day4;
//Logical Operators
public class demo5 {
	 public static void main(String[] args) {
	        boolean a = true;
	        boolean b = false;

	        System.out.println("a && b: " + (a && b)); // Logical AND
	        System.out.println("a || b: " + (a || b)); // Logical OR
	        System.out.println("!a: " + (!a)); // Logical NOT
	    }
}